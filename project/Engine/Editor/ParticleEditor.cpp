#include "ParticleEditor.h"

#include "Engine/objects/particle/ParticleManager.h"

/////////////////////////////////////////////////////////////////////////////////////////
//	コンストラクタ
/////////////////////////////////////////////////////////////////////////////////////////
ParticleEditor::ParticleEditor(){
	editorName_ = "ParticleEditor";
	pParticleManager_ = ParticleManager::GetInstance();
}

/////////////////////////////////////////////////////////////////////////////////////////
//	gui表示
/////////////////////////////////////////////////////////////////////////////////////////
void ParticleEditor::ShowImGuiInterface(){

	// パーティクルマネージャのデバッグUIを表示
	pParticleManager_->ShowDebugUI();
}

/////////////////////////////////////////////////////////////////////////////////////////
//	パーティクルをマネージャに追加
/////////////////////////////////////////////////////////////////////////////////////////
void ParticleEditor::AddParticle4Manager(){
	ParticleSystem* newParticleSystem = new ParticleSystem();

	pParticleManager_->AddSystem(newParticleSystem);
}

/////////////////////////////////////////////////////////////////////////////////////////
//	getter / setter
/////////////////////////////////////////////////////////////////////////////////////////
void ParticleEditor::SetParticleManager(ParticleManager* pParticleManager){
	pParticleManager_ = pParticleManager;
}
